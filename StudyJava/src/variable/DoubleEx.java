package variable;

public class DoubleEx {

	public static void main(String[] args) {
		/*
		 * 자바에는 float실수형과 double실수형이 있음
		 * float실수형은 4byte 32bit, double실수형은 8byte 64bit로
		 * float에 비해 double이 더 정밀하게 실수를 표현할 수 있으으며
		 * 실수형의 기본형은 double형으로 저장되어 있다. 
		 * float을 사용할 때는 double과 구분하기 위해 F나 f를 값 뒤에 붙여서 식별해줘야한다. 
		 * */
		
		double cir1 = 3.14;
		float cir2 = 3.14F;
		System.out.println(cir1);
		System.out.println(cir2);
				

	}

}
